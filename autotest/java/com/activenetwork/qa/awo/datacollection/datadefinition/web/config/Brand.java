public enum Brand {
	//rec.gov
	Rec_gov("NRSO"), 
	
	//ra
	RA,
	
	//pirvate lables
	PLW;
	
	private String name;
	private Brand(String name) {
		this.name=name;
	}
	
	private Brand() {
		this.name=this.toString();
	}
	
	public String getName() {
		return name;
	}
}
