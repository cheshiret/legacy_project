package com.activenetwork.qa.awo.datacollection.legacy.orms.license;

/**
 * @Description: This is the data information for file imports in License Manager
 * @author Lesley Wang
 * @Date  Aug 5, 2013
 */
public class FileImportInfo {
	private String fileType;
	private String fileContent;
	private String fileImportPath;
	private String fileImportID;
	private String fileImportStatus;
	private String fileImportedDate;
	private String numOfFileRecords;
	private String numOfImported;
	private String importFromDate;
	private String importToDate;
	
	/**
	 * @return the fileType
	 */
	public String getFileType() {
		return fileType;
	}
	/**
	 * @param fileType the fileType to set
	 */
	public void setFileType(String fileType) {
		this.fileType = fileType;
	}
	/**
	 * @return the fileContent
	 */
	public String getFileContent() {
		return fileContent;
	}
	/**
	 * @param fileContent the fileContent to set
	 */
	public void setFileContent(String fileContent) {
		this.fileContent = fileContent;
	}
	/**
	 * @return the fileImportPath
	 */
	public String getFileImportPath() {
		return fileImportPath;
	}
	/**
	 * @param fileImportPath the fileImportPath to set
	 */
	public void setFileImportPath(String fileImportPath) {
		this.fileImportPath = fileImportPath;
	}
	/**
	 * @return the fileImportID
	 */
	public String getFileImportID() {
		return fileImportID;
	}
	/**
	 * @param fileImportID the fileImportID to set
	 */
	public void setFileImportID(String fileImportID) {
		this.fileImportID = fileImportID;
	}
	/**
	 * @return the fileImportStatus
	 */
	public String getFileImportStatus() {
		return fileImportStatus;
	}
	/**
	 * @param fileImportStatus the fileImportStatus to set
	 */
	public void setFileImportStatus(String fileImportStatus) {
		this.fileImportStatus = fileImportStatus;
	}
	/**
	 * @return the fileImportedDate
	 */
	public String getFileImportedDate() {
		return fileImportedDate;
	}
	/**
	 * @param fileImportedDate the fileImportedDate to set
	 */
	public void setFileImportedDate(String fileImportedDate) {
		this.fileImportedDate = fileImportedDate;
	}
	/**
	 * @return the numOfFileRecords
	 */
	public String getNumOfFileRecords() {
		return numOfFileRecords;
	}
	/**
	 * @param numOfFileRecords the numOfFileRecords to set
	 */
	public void setNumOfFileRecords(String numOfFileRecords) {
		this.numOfFileRecords = numOfFileRecords;
	}
	/**
	 * @return the numOfImported
	 */
	public String getNumOfImported() {
		return numOfImported;
	}
	/**
	 * @param numOfImported the numOfImported to set
	 */
	public void setNumOfImported(String numOfImported) {
		this.numOfImported = numOfImported;
	}
	/**
	 * @return the importFromDate
	 */
	public String getImportFromDate() {
		return importFromDate;
	}
	/**
	 * @param importFromDate the importFromDate to set
	 */
	public void setImportFromDate(String importFromDate) {
		this.importFromDate = importFromDate;
	}
	/**
	 * @return the importToDate
	 */
	public String getImportToDate() {
		return importToDate;
	}
	/**
	 * @param importToDate the importToDate to set
	 */
	public void setImportToDate(String importToDate) {
		this.importToDate = importToDate;
	}
}
