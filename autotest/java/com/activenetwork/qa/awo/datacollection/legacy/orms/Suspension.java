/**
 * @ScriptName CustomerSuspension.java
 * @Date:Feb 15, 2011
 * @Description:this class is created for customer suspension in LM
 * @author asun
 */
public class Suspension implements Comparable<Suspension> {

	public String id = "";

	public String status = "";

	public String type = "";

	public String beginDate = "";

	public String endDate = "";

	public String datePosted = "";

	public String details = "";
	
	public String comment = "";
	
	public String creationDateTime = "";
	
	public String creationUser = "";
	
	public String[] identifiersTypes= null;
	public String[] identifiersNums= null;

	@Override
	public int compareTo(Suspension o) {
		return this.comment.compareTo(o.comment);
	}
}
