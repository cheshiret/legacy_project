package com.activenetwork.qa.awo.datacollection.legacy.orms;
/**
 * @Description: This is a class to store the search filter of "vocher/Internal GC Record" of invoices
 * @author qchen
 * @Date  August 27, 2012
 */
public class InvoiceVoucherInternalGCRecordSearchFilter {
	private String voucherID;
	private String GCOrdNum;
	private String remittanceNum;
	private String accountCode;
	private String accountDesc;
	/**
	 * @return the voucherID
	 */
	public String getVoucherID() {
		return voucherID;
	}
	/**
	 * @param voucherID the voucherID to set
	 */
	public void setVoucherID(String voucherID) {
		this.voucherID = voucherID;
	}
	/**
	 * @return the gCOrdNum
	 */
	public String getGCOrdNum() {
		return GCOrdNum;
	}
	/**
	 * @param gCOrdNum the gCOrdNum to set
	 */
	public void setGCOrdNum(String gCOrdNum) {
		GCOrdNum = gCOrdNum;
	}
	/**
	 * @return the remittanceNum
	 */
	public String getRemittanceNum() {
		return remittanceNum;
	}
	/**
	 * @param remittanceNum the remittanceNum to set
	 */
	public void setRemittanceNum(String remittanceNum) {
		this.remittanceNum = remittanceNum;
	}
	/**
	 * @return the accountCode
	 */
	public String getAccountCode() {
		return accountCode;
	}
	/**
	 * @param accountCode the accountCode to set
	 */
	public void setAccountCode(String accountCode) {
		this.accountCode = accountCode;
	}
	/**
	 * @return the accountDesc
	 */
	public String getAccountDesc() {
		return accountDesc;
	}
	/**
	 * @param accountDesc the accountDesc to set
	 */
	public void setAccountDesc(String accountDesc) {
		this.accountDesc = accountDesc;
	}

	
	
}
